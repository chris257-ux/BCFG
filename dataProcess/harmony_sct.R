load_and_create_seurat=function(data_dir,samples,project_name){
data_list=lapply(samples,function(sample){
Read10X(data.dir=file.path(base_dir,data_dir,sample))
})
seurat_list=lapply(seq_along(data_list),function(i){
seurat_obj=CreateSeuratObject(counts=data_list[[i]],project=project_name,min.cells=3,min.features=200)
seurat_obj$sample_name=samples[i]
seurat_obj[["percent.mt"]]=PercentageFeatureSet(seurat_obj,pattern="^MT-")
seurat_obj=subset(seurat_obj,subset=nFeature_RNA>200&percent.mt<25&nCount_RNA>800)
return(seurat_obj)
})
merged_seurat=merge(seurat_list[[1]],y=seurat_list[-1],add.cell.ids=samples)
return(merged_seurat)
}
